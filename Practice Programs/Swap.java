import java.util.Scanner;
class Swapno{
public static void main(String[] args) 
{ 
   Scanner sc = new Scanner(System.in);
    int x = sc.nextInt();	
    int y = sc.nextInt();
    x = x*y;//200
    y = x/y;//10
    x = x/y;//20
    System.out.println(x);
    System.out.println(y);
}   
}
		